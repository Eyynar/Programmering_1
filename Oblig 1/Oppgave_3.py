#Ber bruker om å skrive inn ett nummer, dette nummerert blir omgjort til en float.
first_number = float(input("Enter a number:"))

#Ber bruker om å skrived inn det andre nummerert, som også blir gjort om til en float
second_nymber = float(input("Enter another number:"))

#Utfører de forskjellige matematiske operasjonene med de valgte tallene.
print(first_number * second_nymber)
print(first_number / second_nymber)
print(first_number + second_nymber)
print(first_number - second_nymber)
print(first_number % second_nymber)
print(first_number ** second_nymber)
print(first_number // second_nymber)